# arithmetic operator,assignment operator,logical operator,comparison operator

num = 43
num2 = 12

# Arithmetic operator
print(f"the sum of {num} and {num2} is {num + num2} ")
print(f"the difference of {num} and {num2} is {num - num2} ")
print(f"the division of {num} and {num2} is {num / num2} ")
print(f"the multiplication of {num} and {num2} is {num * num2} ")
print(f"the remainder of {num} and {num2} is {num % num2} ")
num3 = 56
num4 = 23

# comparison operator
print(f"{num3} and {num4} are equal {num3 == num4} ")
print(f"{num3} and {num4} are not equal {num3 != num4} ")
print(f"{num3} is greater than  {num4} {num3 > num4} ")
print(f"{num3} is less than {num4} {num3 < num4} ")
print(f"{num3} is greater than or equal to  {num4} {num3 > -num4} ")
print(f"{num3} is less than or equal to{num4} {num3 <= num4} ")

# assignment operator
num5 = 27
num6 = 8

print(f"num5 = num6 is {num5 == num6} ")
print(f"num5 += num6 is {num5 + num6} ")
print(f"num5 *= num6 is {num5 * num6} ")
print(f"num5 -= num6 is {num5 - num6} ")

# logical operators
num7 = 10

print(f"one statement is true {num7 >= 15 or num7 < 12}")
print(f"both statements are true {num7 >= 15 and num7 < 12}")
